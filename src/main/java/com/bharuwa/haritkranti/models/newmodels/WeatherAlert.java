package com.bharuwa.haritkranti.models.newmodels;

public class WeatherAlert {
	
	private String range;
	private String alertInEnglish;
	private String displayAlert;
	private String alertInHindi;
	private String minRange;
	private String maxRange;
	
	public String getRange() {
		return range;
	}
	public void setRange(String range) {
		this.range = range;
	}
	public String getAlertInEnglish() {
		return alertInEnglish;
	}
	public void setAlertInEnglish(String alertInEnglish) {
		this.alertInEnglish = alertInEnglish;
	}
	public String getAlertInHindi() {
		return alertInHindi;
	}
	public void setAlertInHindi(String alertInHindi) {
		this.alertInHindi = alertInHindi;
	}
	public String getMinRange() {
		return minRange;
	}
	public void setMinRange(String minRange) {
		this.minRange = minRange;
	}
	public String getMaxRange() {
		return maxRange;
	}
	public void setMaxRange(String maxRange) {
		this.maxRange = maxRange;
	}
	public String getDisplayAlert() {
		return displayAlert;
	}
	public void setDisplayAlert(String displayAlert) {
		this.displayAlert = displayAlert;
	}
	
}
